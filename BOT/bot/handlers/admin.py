        message_text += f"üîë –ê–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–æ–≤: {total_admins}\n\n"
        message_text += f"üìù –í—Å–µ–≥–æ –∑–∞–¥–∞—á: {total_tasks}\n"
        message_text += f"‚úÖ –ó–∞–≤–µ—Ä—à–µ–Ω–æ –∑–∞–¥–∞—á: {completed_tasks}\n"
        message_text += f"üîî –í—Å–µ–≥–æ –Ω–∞–ø–æ–º–∏–Ω–∞–Ω–∏–π: {total_reminders}\n"
